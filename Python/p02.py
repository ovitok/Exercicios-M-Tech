# Par ou Ímpar

numero = int(input("Digite um número de 1 até 10: "))

if numero == 1:
    print("Seu número é ímpar!")
elif numero == 3:
    print("Seu número é ímpar!") 
elif numero == 5:
    print("Seu número é ímpar!")   
elif numero == 7:
    print("Seu número é ímpar!")
elif numero == 9:
    print("Seu número é ímpar!")
else:
    print("Seu número é par!")
